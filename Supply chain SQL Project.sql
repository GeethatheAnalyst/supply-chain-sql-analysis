### 2.	Display month and year wise count of the orders placed

SELECT 
    year(ORDERDATE) AS Order_Year,
    MONTH(OrderDate) AS Order_Month,
    COUNT(*) AS ORDERS_PLACED
FROM Orders
GROUP BY ORDER_YEAR , ORDER_MONTH;

### 3.	Which customers did not place any order.

