### 4.	Arrange the products based on high demand by the customer in year 2021.

-- SUMMING THE OI.QUANTITY TO CALCULATE THE DEMAND // 

SELECT 
    OI.ProductId,
    P.ProductName,
    SUM(OI.Quantity) AS ORDER_DEMAND,
    YEAR(O.OrderDate) AS ORDER_YEAR
FROM
orderitem AS OI
INNER JOIN orders AS O
ON O.Id = OI.OrderId
INNER JOIN product AS P 
ON P.Id = OI.ProductId
WHERE YEAR(O.OrderDate)= 2021
GROUP BY OI.ProductId , P.ProductName , YEAR(O.ORDERDATE)
ORDER BY ORDER_DEMAND DESC;

-- INCASE WE NEED TOP 10 OF HIGH DEMAND, USING LIMIT 10 , 

SELECT 
    OI.ProductId,
    P.ProductName,
    SUM(OI.Quantity) AS ORDER_DEMAND,
    YEAR(O.OrderDate) AS ORDER_YEAR
FROM
orderitem AS OI
INNER JOIN orders AS O
ON O.Id = OI.OrderId
INNER JOIN product AS P 
ON P.Id = OI.ProductId
WHERE YEAR(O.OrderDate) = 2021
GROUP BY OI.ProductId , P.ProductName , YEAR(O.ORDERDATE)
ORDER BY ORDER_DEMAND DESC LIMIT 10;



### 5. Which customer has placed the largest order (i.e. highest order amount)

-- a) using subquery 
SELECT 
    c.Id,
    CONCAT(C.FirstName,'  ',C.LastName) AS FULLNAME,
    c.Country,
    o.TotalAmount
FROM orders o
JOIN customer c
    ON o.CustomerId = c.Id
WHERE o.TotalAmount = (SELECT MAX(TotalAmount) FROM orders);

-- using groupby & order by

SELECT 
	  C.Id, CONCAT(C.FirstName,'  ',C.LastName) AS FULLNAME,C.CITY,C.Country,
      MAX(O.TotalAmount) AS HIGHEST_TOTAL_AMOUNT
FROM CUSTOMER C
JOIN ORDERS O 
ON C.ID = O.CUSTOMERID
GROUP BY C.ID,FULLNAME,C.CITY,C.COUNTRY
ORDER BY HIGHEST_TOTAL_AMOUNT DESC LIMIT 1;


