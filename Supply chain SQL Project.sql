### 7.	Find out top 3 suppliers in terms of revenue generated by their products.

SELECT S.Id,S.CompanyName,S.ContactName,CONCAT(S.City,' ',S.Country) AS LOCATION ,S.Phone,
	   P.ProductName,
       SUM(OI.UnitPrice * OI.Quantity) AS TOTAL_REVENUE
FROM SUPPLIER AS S
INNER JOIN PRODUCT AS P 
ON S.ID = P.SUPPLIERID
INNER JOIN orderitem AS OI 
ON P.ID = OI.ProductId
group by S.Id,S.CompanyName,S.ContactName, LOCATION ,S.Phone, P.ProductName
ORDER BY TOTAL_REVENUE DESC LIMIT 3;


-- USING RANK() WINDOWS 

SELECT * 
FROM (
      SELECT S.Id,S.CompanyName,S.ContactName,CONCAT(S.City,' ',S.Country) AS LOCATION ,S.Phone,
	   P.ProductName,
       SUM(OI.UnitPrice * OI.Quantity) AS TOTAL_REVENUE,
       RANK() OVER (ORDER BY SUM(OI.UnitPrice * OI.Quantity) DESC) AS RANK_NUM
FROM SUPPLIER AS S
INNER JOIN PRODUCT AS P 
ON S.ID = P.SUPPLIERID
INNER JOIN orderitem AS OI 
ON P.ID = OI.ProductId
group by S.Id,S.CompanyName,S.ContactName, LOCATION ,S.Phone, P.ProductName
) AS TOP_SUPPLIER
WHERE RANK_NUM <=3;


