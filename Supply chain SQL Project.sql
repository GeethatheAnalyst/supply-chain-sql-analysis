### 9. Are there any customers who ordered more than 10 different products in the single order

SELECT 
    C.Id,CONCAT(C.FirstName,' ',C.LastName) AS FULLNAME,C.City,C.Country,
    O.Id AS OrderId,
    COUNT(DISTINCT oi.ProductId) AS ORDERED_PRODUCT
FROM customer AS C
JOIN orders AS O ON C.Id = O.CustomerId
JOIN orderitem OI ON O.Id = OI.OrderId
GROUP BY C.Id, FULLNAME, C.City, C.Country, O.Id
HAVING ORDERED_PRODUCT > 10
ORDER BY ORDERED_PRODUCT DESC;


### 10. Company sells the product at different discounted rates. Refer actual product price in product table 
###    and selling price in the order item table. Find the total amount saved in each order then 
###    display the orders from highest to lowest amount saved. 


SELECT
    OI.OrderId AS ORDER_ID,
    SUM((OI.quantity * P.UnitPrice) - (OI.quantity * OI.UnitPrice)) AS AMOUNT_SAVED
FROM orderitem AS OI
JOIN product AS P
ON OI.ProductId = P.Id
group by OI.OrderId
ORDER BY AMOUNT_SAVED DESC ;


### 11.	 List the Suppliers such that there are no customers in the country of the supplier


SELECT
    S.Id AS SUPPLIER_ID,S.CompanyName,S.ContactName AS SUPPLIER_NAME, S.Country,C.ID AS CUSTOMER_ID
FROM Supplier S
LEFT JOIN Customer AS C
ON S.Country = C.Country
WHERE C.Id IS NULL;

### 12. Find out for which products, UK is dependent on other countries for the supply. 
###    List the countries which are supplying these products in the same list.

  
SELECT
	P.Id AS PRODUCT_ID,P.ProductName,
    CONCAT(C.FirstName, ' ', C.LastName) AS CUSTOMER_NAME,C.Country AS CUSTOMER_COUNTRY,
	S.Country AS SUPPLIER_COUNTRY
FROM Customer AS C
JOIN Orders AS O
ON C.Id = O.CustomerId
JOIN OrderItem AS OI
ON O.Id = OI.OrderId
JOIN Product AS P
ON OI.ProductId = P.Id
JOIN Supplier AS S
ON P.SupplierId = S.Id
WHERE C.Country = 'UK'
AND S.Country NOT IN ('UK');



